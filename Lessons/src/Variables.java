public class Variables {
    public static void main(String[] args) {
        int myInt;
        myInt = 15;
        System.out.println(myInt);

        int myInt2 = 20;              // 32 бита
        System.out.println(myInt2);

        short myShort = 30;  // 16 бит
        long myLong = 1000;  // 64 бита

        double myDouble = 235.50; // 64
        float myFloat = 159.56f;  // 32

        char c = 'c';
        boolean b = true;    //  false
        boolean b2 = false;  //  true

        byte myByte = 8;     // 8 бит

    }
}
